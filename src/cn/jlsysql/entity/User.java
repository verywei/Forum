@Component
public class User implements Serializable {

    String id, username, password,img ,book_amount,resource_amount,nickname;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getBook_amount() {
        return book_amount;
    }

    public void setBook_amount(String book_amount) {
        this.book_amount = book_amount;
    }

    public String getResource_amount() {
        return resource_amount;
    }

    public void setResource_amount(String resource_amount) {
        this.resource_amount = resource_amount;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    @Override
    public String toString() {
        return "User{" +
                "id='" + id + '\'' +
                ", username='" + username + '\'' +
                ", password='" + password + '\'' +
                ", img='" + img + '\'' +
                ", book_amount='" + book_amount + '\'' +
                ", resource_amount='" + resource_amount + '\'' +
                ", nickname='" + nickname + '\'' +
                '}';
    }
}
